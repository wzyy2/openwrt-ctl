void OLED_Refresh_Gram(void)  
{  
    u8 ii,nn;  
    for(ii=0;ii<8;ii++)  
    {  
        OLED_WRCMD(0xb0+ii);  
        OLED_WRCMD(0x00);  
        OLED_WRCMD(0x10);  
        for(nn=0;nn<120;nn++)OLED_WRDATA(OLED_GRAM[nn][ii]);  
 }  
}  
void OLED_DRAWPoint(u8 x,u8 y,u8 t)  
{  
    u8 pos,bx,temp=0;  
   if(x>127||y>63)return;  
    pos=y/8;  
    bx=y%8;  
    temp=1<<(7-bx);  
    if(t)OLED_GRAM[x][pos]|=temp;  
    else OLED_GRAM[x][pos]&=~temp;  
}  

